def sort_numbered_text(text):
    lines = [line.strip() for line in text.split('\n') if line.strip()]
    numbered_lines = []
    for line in lines:
        parts = line.split('.', 1)
        if len(parts) == 2:
            try:
                number = int(parts[0])
                text = parts[1].strip()
                numbered_lines.append((number, text))
            except ValueError:
                continue
    return sorted(numbered_lines, key=lambda x: x[0])

